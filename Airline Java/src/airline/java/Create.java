    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
    String id = usid.getText().trim();
    String firstName = txtfn.getText().trim();
    String lastName = txtln.getText().trim();
    String userName = txtusr.getText().trim();
    String password = txtpass.getText().trim();

    // === Input Validation ===
    if (firstName.isEmpty() || lastName.isEmpty() || userName.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        Class.forName("com.mysql.jdbc.Driver");
        java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost/airline", "root", "");
        java.sql.PreparedStatement pst = con.prepareStatement("INSERT INTO user(id, firstname, lastname, username, password) VALUES(?,?,?,?,?)");

        pst.setString(1, id);
        pst.setString(2, firstName);
        pst.setString(3, lastName);
        pst.setString(4, userName);
        pst.setString(5, password);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "User Created Successfully");

        // Reset Fields
        txtfn.setText("");
        txtln.setText("");
        txtusr.setText("");
        txtpass.setText("");

        autoID(); // Call method to generate new User ID

    } catch (ClassNotFoundException | java.sql.SQLException ex) {
        java.util.logging.Logger.getLogger(Create.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage(), "DB Error", JOptionPane.ERROR_MESSAGE);
    }    
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


