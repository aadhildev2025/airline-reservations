    public void autoID() {
    try {
        Class.forName("com.mysql.jdbc.Driver");
        java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost/airline", "root", "");
        java.sql.Statement s = con.createStatement();
        java.sql.ResultSet rs = s.executeQuery("SELECT MAX(id) FROM user");
        rs.next();
        String maxId = rs.getString(1);

        if (maxId == null) {
            usid.setText("U001");
        } else {
            long id = Long.parseLong(maxId.substring(1)) + 1;
            usid.setText("U" + String.format("%03d", id));
        }

    } catch (ClassNotFoundException | java.sql.SQLException ex) {
        java.util.logging.Logger.getLogger(Create.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
    }
}
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    

