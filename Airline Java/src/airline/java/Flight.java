    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
            
    String id = txtfid.getText();
    String flightname = txtfn.getText();
    String Source = txtsource.getSelectedItem().toString().trim();
    String Departure = txtdep.getSelectedItem().toString().trim();
    String Date = txtdate.getText();
    String DepartureTime = txtdeptm.getText();
    String ArrivalTime = txtarrtm.getText();
    String FlightCharge = txtflightch.getText();


    // Validate empty fields
    if (id.isEmpty() || flightname.isEmpty() || Source.isEmpty() || Departure.isEmpty() || Date.isEmpty() || 
    DepartureTime.isEmpty() || ArrivalTime.isEmpty() || FlightCharge.isEmpty()) {

        
        JOptionPane.showMessageDialog(this, "Please fill in all fields!", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    
    try {
        Class.forName("com.mysql.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost/airline", "root", "");  

        pst = con.prepareStatement("INSERT into flight(id, flightname, source, departure, date, deptime, arrtime, flightcharge) VALUES (?, ?, ?, ?, ?, ?, ?, ?)");

        pst.setString(1, id);
        pst.setString(2, flightname);
        pst.setString(3, Source);
        pst.setString(4, Departure);
        pst.setString(5, Date);
        pst.setString(6, DepartureTime);
        pst.setString(7, ArrivalTime);
        pst.setString(8, FlightCharge + "LKR");


        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Flight Added Successfully!");

        

    } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    
     
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

