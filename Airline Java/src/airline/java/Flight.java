    public void autoid() {
    try {
        Class.forName("com.mysql.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost/airline", "root", "");
        java.sql.Statement s = con.createStatement();
        ResultSet rs = s.executeQuery("SELECT MAX(id) FROM flight");
        rs.next();
        String maxId = rs.getString(1); // Use column index 1 instead of "MAX(id)"

        if (maxId == null) {
            txtfid.setText("FL001");
        } else {
            long id = Long.parseLong(maxId.substring(2));
            id++;
            txtfid.setText("FL" + String.format("%03d", id));
        }
    } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    
    

